/**
 * windows-debloater.js — Windows Debloater (GUI to Script)
 * Generates a downloadable PowerShell script only — never executes OS commands.
 */

const WindowsDebloater = (() => {
  const TOOL_LOG_NAME = 'Windows Debloater';
  const DOWNLOAD_NAME = 'debloat_optimizer.ps1';

  const COMMAND_MAP = {
    telemetry: [
      '# --- Remove Windows Telemetry ---',
      'Stop-Service -Name DiagTrack -Force -ErrorAction SilentlyContinue',
      'Set-Service -Name DiagTrack -StartupType Disabled -ErrorAction SilentlyContinue',
      'Stop-Service -Name dmwappushservice -Force -ErrorAction SilentlyContinue',
      'Set-Service -Name dmwappushservice -StartupType Disabled -ErrorAction SilentlyContinue',
      'Set-ItemProperty -Path "HKLM:\\SOFTWARE\\Policies\\Microsoft\\Windows\\DataCollection" -Name AllowTelemetry -Type DWord -Value 0 -Force',
      'New-ItemProperty -Path "HKLM:\\SOFTWARE\\Microsoft\\Windows\\CurrentVersion\\Policies\\DataCollection" -Name AllowTelemetry -PropertyType DWord -Value 0 -Force -ErrorAction SilentlyContinue',
    ],
    bloatware: [
      '# --- Uninstall Bloatware Apps ---',
      'Get-AppxPackage *bing* | Remove-AppxPackage -ErrorAction SilentlyContinue',
      'Get-AppxPackage *zune* | Remove-AppxPackage -ErrorAction SilentlyContinue',
      'Get-AppxPackage *solitaire* | Remove-AppxPackage -ErrorAction SilentlyContinue',
      'Get-AppxPackage *officehub* | Remove-AppxPackage -ErrorAction SilentlyContinue',
      'Get-AppxPackage *skypeapp* | Remove-AppxPackage -ErrorAction SilentlyContinue',
      'Get-AppxPackage *xbox* | Remove-AppxPackage -ErrorAction SilentlyContinue',
      'Get-AppxPackage *clipchamp* | Remove-AppxPackage -ErrorAction SilentlyContinue',
      'Get-AppxPackage *gamingapp* | Remove-AppxPackage -ErrorAction SilentlyContinue',
    ],
    cortana: [
      '# --- Disable Cortana ---',
      'Set-ItemProperty -Path "HKLM:\\SOFTWARE\\Policies\\Microsoft\\Windows\\Windows Search" -Name AllowCortana -Type DWord -Value 0 -Force',
      'New-ItemProperty -Path "HKCU:\\SOFTWARE\\Microsoft\\Windows\\CurrentVersion\\Search" -Name BingSearchEnabled -PropertyType DWord -Value 0 -Force -ErrorAction SilentlyContinue',
      'New-ItemProperty -Path "HKCU:\\SOFTWARE\\Microsoft\\Windows\\CurrentVersion\\Search" -Name CortanaConsent -PropertyType DWord -Value 0 -Force -ErrorAction SilentlyContinue',
      'Stop-Service -Name "WSearch" -Force -ErrorAction SilentlyContinue',
      'Set-Service -Name "WSearch" -StartupType Manual -ErrorAction SilentlyContinue',
    ],
    network: [
      '# --- Optimize Network Latency ---',
      'netsh int tcp set global autotuninglevel=normal',
      'netsh int tcp set global chimney=enabled',
      'netsh int tcp set global dca=enabled',
      'netsh int tcp set global netdma=enabled',
      'netsh int tcp set global rss=enabled',
      'netsh int tcp set global timestamps=disabled',
      'New-ItemProperty -Path "HKLM:\\SYSTEM\\CurrentControlSet\\Services\\Tcpip\\Parameters" -Name TcpAckFrequency -PropertyType DWord -Value 1 -Force -ErrorAction SilentlyContinue',
      'New-ItemProperty -Path "HKLM:\\SYSTEM\\CurrentControlSet\\Services\\Tcpip\\Parameters\\Interfaces" -Name TcpDelAckTicks -PropertyType DWord -Value 0 -Force -ErrorAction SilentlyContinue',
    ],
  };

  const OPTION_KEYS = ['telemetry', 'bloatware', 'cortana', 'network'];

  function getSelectedOptions() {
    return OPTION_KEYS.filter((key) => {
      const el = document.getElementById('debloat-opt-' + key);
      return el && el.checked;
    });
  }

  function buildScript(selected) {
    const lines = [
      '# debloat_optimizer.ps1',
      '# Generated by PIXEL.TOOLS — Windows Debloater',
      '# Run this script in an elevated PowerShell session (Run as Administrator).',
      '# Review every command before executing. PIXEL.TOOLS does not run this script for you.',
      '',
      'Set-StrictMode -Version Latest',
      '$ErrorActionPreference = "Continue"',
      '',
    ];

    selected.forEach((key) => {
      const block = COMMAND_MAP[key];
      if (!block) return;
      lines.push(...block, '');
    });

    lines.push('Write-Host "Debloat optimizer sections applied. Reboot recommended." -ForegroundColor Green');
    return lines.join('\r\n');
  }

  function downloadScript(content) {
    const blob = new Blob([content], { type: 'application/octet-stream' });
    const url = URL.createObjectURL(blob);
    const anchor = document.createElement('a');
    anchor.href = url;
    anchor.download = DOWNLOAD_NAME;
    anchor.style.display = 'none';
    document.body.appendChild(anchor);
    anchor.click();
    document.body.removeChild(anchor);
    URL.revokeObjectURL(url);
  }

  function setStatus(msg, color) {
    const el = document.getElementById('debloat-status-text');
    if (el) {
      el.textContent = msg;
      el.style.color = color || 'var(--gray-light)';
    }
  }

  function generateOptimizer() {
    const selected = getSelectedOptions();
    if (!selected.length) {
      setStatus('SELECT AT LEAST ONE MODULE BEFORE GENERATING.', 'var(--yellow)');
      if (typeof Toast !== 'undefined') Toast.show('SELECT A MODULE', 'warning');
      return;
    }

    const script = buildScript(selected);
    downloadScript(script);
    setStatus(
      'SCRIPT READY — ' + DOWNLOAD_NAME + ' downloaded (' + selected.length + ' module' + (selected.length === 1 ? '' : 's') + '). Run in elevated PowerShell after review.',
      'var(--green)'
    );
    if (typeof Toast !== 'undefined') Toast.show('OPTIMIZER GENERATED', 'success');
    if (typeof PixelAudio !== 'undefined') PixelAudio.copy();
    logUsage();
  }

  async function logUsage() {
    try {
      const mod = await import('../supabase-config.js');
      await mod.logToolUsage(TOOL_LOG_NAME);
    } catch (_) { /* fire-and-forget */ }
  }

  function bindEvents() {
    document.getElementById('debloat-generate')?.addEventListener('click', () => {
      if (typeof PixelAudio !== 'undefined') PixelAudio.click();
      generateOptimizer();
    });
  }

  function init() {
    const panel = document.getElementById('tool-windows-debloater');
    if (!panel || panel.dataset.inited) return;
    panel.dataset.inited = 'true';
    bindEvents();
    setStatus('CHECK MODULES AND PRESS GENERATE OPTIMIZER — output is a local .ps1 download only.', 'var(--gray-light)');
  }

  return {
    init,
    generateOptimizer,
    buildScript,
    getSelectedOptions,
    COMMAND_MAP,
    DOWNLOAD_NAME,
  };
})();
