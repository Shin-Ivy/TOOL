#!/usr/bin/env node
/**
 * sync-supabase-env.mjs
 * Reads pixel-dashboard/.env.local and writes js/supabase-secrets.js (gitignored).
 */

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const ENV_PATH = join(ROOT, '.env.local');
const OUT_PATH = join(ROOT, 'js', 'supabase-secrets.js');

const URL_KEYS = [
  'SUPABASE_URL',
  'VITE_SUPABASE_URL',
  'NEXT_PUBLIC_SUPABASE_URL',
];

const ANON_KEYS = [
  'SUPABASE_ANON_KEY',
  'SUPABASE_PUBLISHABLE_KEY',
  'VITE_SUPABASE_ANON_KEY',
  'VITE_SUPABASE_PUBLISHABLE_KEY',
  'NEXT_PUBLIC_SUPABASE_ANON_KEY',
  'NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY',
];

const PLACEHOLDER_PATTERNS = [
  /^YOUR_/i,
  /^your_/i,
  /YOUR_PROJECT_REF/i,
  /placeholder/i,
  /^<.*>$/,
];

function parseEnvFile(content) {
  const vars = {};
  for (const line of content.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const m = trimmed.match(/^(?:export\s+)?([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*)$/);
    if (!m) continue;
    let value = m[2].trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    vars[m[1]] = value;
  }
  return vars;
}

function pickFirst(vars, keys) {
  for (const key of keys) {
    const v = vars[key];
    if (v && String(v).trim()) return String(v).trim();
  }
  return '';
}

function isPlaceholder(value) {
  const v = String(value).trim();
  if (!v) return true;
  return PLACEHOLDER_PATTERNS.some((re) => re.test(v));
}

function escapeJsString(value) {
  return value
    .replace(/\\/g, '\\\\')
    .replace(/'/g, "\\'")
    .replace(/\r/g, '\\r')
    .replace(/\n/g, '\\n');
}

function main() {
  if (!existsSync(ENV_PATH)) {
    console.warn('[sync-supabase-env] No .env.local found at pixel-dashboard/.env.local');
    console.warn('[sync-supabase-env] Copy .env.local.example and add SUPABASE_URL + SUPABASE_ANON_KEY');
    process.exit(0);
  }

  let raw;
  try {
    raw = readFileSync(ENV_PATH, 'utf8');
  } catch (err) {
    console.error('[sync-supabase-env] Could not read .env.local:', err.message);
    process.exit(1);
  }

  const vars = parseEnvFile(raw);
  const url = pickFirst(vars, URL_KEYS);
  const anonKey = pickFirst(vars, ANON_KEYS);

  if (!url || !anonKey || isPlaceholder(url) || isPlaceholder(anonKey)) {
    console.error('[sync-supabase-env] .env.local exists but SUPABASE_URL and/or anon key are missing or still placeholders.');
    console.error('[sync-supabase-env] Set SUPABASE_URL and SUPABASE_ANON_KEY (or VITE_/NEXT_PUBLIC_ variants).');
    process.exit(1);
  }

  if (!url.startsWith('https://')) {
    console.error('[sync-supabase-env] SUPABASE_URL must start with https://');
    process.exit(1);
  }

  const out = `/**
 * supabase-secrets.js — AUTO-GENERATED (do not edit)
 * Generated by: npm run sync:supabase
 * Source: pixel-dashboard/.env.local
 */

export const SUPABASE_URL = '${escapeJsString(url)}';
export const SUPABASE_ANON_KEY = '${escapeJsString(anonKey)}';
`;

  writeFileSync(OUT_PATH, out, 'utf8');
  console.log('[sync-supabase-env] Wrote js/supabase-secrets.js');
}

main();
